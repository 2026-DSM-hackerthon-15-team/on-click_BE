    ON instagram_integrations (token_expires_at);

CREATE TABLE instagram_oauth_states (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    state_hash VARCHAR(64) NOT NULL,
    store_id BIGINT NOT NULL,
    user_id BIGINT NOT NULL,
    expires_at TIMESTAMP WITH TIME ZONE NOT NULL,
    used_at TIMESTAMP WITH TIME ZONE,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT uq_instagram_oauth_state_hash UNIQUE (state_hash),
    CONSTRAINT fk_instagram_oauth_states_store FOREIGN KEY (store_id) REFERENCES stores (id) ON DELETE CASCADE,
    CONSTRAINT fk_instagram_oauth_states_user FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE
);

CREATE INDEX idx_instagram_oauth_states_expiry
    ON instagram_oauth_states (expires_at);
